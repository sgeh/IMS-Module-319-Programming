int i = 5;

// i ≙ 5
Console.WriteLine($"{++i}");

// i ≙ 6
Console.WriteLine($"{5 + ++i}");

// i ≙ 7
Console.WriteLine($"{++i + 7}");

// i ≙ 8
Console.WriteLine($"{i++}");

// i ≙ 9
Console.WriteLine($"{5 + i++}");

// i ≙ 10
Console.WriteLine($"{i++ + 7}");

// i ≙ 11
Console.WriteLine($"{++i + i++}");

// i ≙ 13